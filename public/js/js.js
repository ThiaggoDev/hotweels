    // Captura el clic en el enlace de búsqueda.
    var enlaceResultados = document.getElementById("enlace_resultados");
    enlaceResultados.addEventListener("click", function(event) {
    event.preventDefault(); // Evita que el enlace navegue a la página directamente
    
    // Obtén el valor de búsqueda
    var busqueda = document.getElementById("barra_busqueda").value;
    
    // Redirige a la página de resultados con el término de búsqueda en la URL
    window.location.href = "./js/resultados.html?query=" + encodeURIComponent(busqueda);
    });

